– améliorer le contenu, l’ergonomie et les performances du site,
– mesurer l’audience et les statistiques de fréquentation,
– assurer la sécurité et le bon fonctionnement technique du site.

Base légale des traitements

Les traitements réalisés reposent sur :
– le consentement de l’utilisateur lorsqu’il envoie une demande via le formulaire de contact ou accepte les cookies de mesure d’audience,

– Logs techniques : conservés pour une durée limitée, strictement nécessaire à la sécurité et au bon fonctionnement du site.

Cookies

Un cookie est un petit fichier texte enregistré sur votre appareil lors de votre visite sur un site.

Le site DigiJet peut utiliser :
– des cookies techniques, nécessaires au bon fonctionnement du site (par exemple pour la navigation, la sécurité, la gestion de session),
– des cookies de mesure d’audience, si un outil comme Google Analytics est installé.

Vous pouvez configurer votre navigateur pour accepter ou refuser les cookies, ou pour être informé lorsqu’un cookie est déposé.
Le refus des cookies techniques peut perturber le fonctionnement du site. Le refus des cookies de mesure d’audience n’empêche pas l’accès au site.

Sécurité des données

DigiJet met en œuvre des moyens raisonnables pour protéger les données personnelles contre la perte, l’accès non autorisé, l’utilisation abusive, la modification ou la divulgation.

Le site est développé sur mesure et hébergé chez un prestataire reconnu. Des mises à jour techniques et des mesures de sécurité sont appliquées autant que possible.
Cependant, aucun système n’étant totalement sécurisé, DigiJet ne peut garantir une sécurité absolue des données transmises via Internet.

Les messages envoyés via le formulaire sont transmis directement par email à DigiJet via une adresse professionnelle sécurisée (contact@digijet.fr
